package net.visualillusionsent.realms.io;

/**
 * Data Constructor
 * 
 * @author darkdiplomat
 */
public abstract class RealmsData {
    abstract public boolean dumppoly();
    abstract public boolean dumpzone();
    abstract public boolean dumpperm();
    abstract public boolean dumpinv();
    abstract public boolean reloadAll();
    abstract public boolean reloadZones();
    abstract public boolean reloadPerms();
    abstract public boolean reloadPolys();
}


